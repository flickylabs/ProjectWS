export function buildWitnessFewShotBlock(
  slot: WitnessSlot,
  depth: WitnessDepth,
  limit = 2,
): string {
  const samples = WITNESS_FEW_SHOT_EXAMPLES[slot]?.[depth]?.slice(0, limit) ?? []
  if (samples.length === 0) return ''

  const lines = ['## 유형/깊이별 증언 예시 (말투와 정보량을 이 리듬에 맞춰라)']
  for (const [index, sample] of samples.entries()) {
    lines.push(`예시${index + 1}`)
    lines.push(`- 재판관 질문: "${sample.question}"`)
    lines.push(`- 증인 응답: "${sample.response}"`)
    if (sample.behaviorHint) {
      lines.push(`- behaviorHint: "${sample.behaviorHint}"`)
    }
  }
  return lines.join('\n')
}

export function buildHiddenAgendaPatternBlock(slot: WitnessSlot): string {
  const patterns = WITNESS_HIDDEN_AGENDA_PATTERNS[slot] ?? []
  if (patterns.length === 0) return ''

  return [
    '## hiddenAgenda가 있을 때 드러나는 방식',
    '- 숨기고 싶은 것이 있으면 단순 무응답으로 끝내지 말고, 말을 고르다 멈추거나 화제를 비트는 식으로 적극적으로 회피하라.',
    ...patterns.map((pattern, index) => `${index + 1}. ${pattern}`),
  ].join('\n')
}
