#!/usr/bin/env node
const fs = require('fs')
const path = require('path')
const { cleanSentence, paragraph, hasJongseong } = require('./scripted-korean-utils.cjs')

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'))
}

function writeJson(filePath, value) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true })
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, 'utf8')
}

function makeEntry(speaker, text, relatedDisputes = [], behaviorHint = null) {
  return { speaker, text, relatedDisputes, behaviorHint }
}

function clip(text, max = 84) {
  const cleaned = cleanSentence(text || '')
  if (cleaned.length <= max) return cleaned
  return `${cleaned.slice(0, max - 1).trim()}…`
}

function safeName(name) {
  const cleaned = cleanSentence(name || '')
  if (!cleaned) return '상대'
  return /씨$/u.test(cleaned) ? cleaned : `${cleaned} 씨`
}

function quoted(text) {
  return `‘${cleanSentence(text || '')}’`
}

function issue(text) {
  return `${quoted(text)} 문제`
}

function objectPhrase(text) {
  const cleaned = cleanSentence(text || '')
  return `${cleaned}${hasJongseong(cleaned) ? '을' : '를'}`
}

function topicPhrase(text) {
  const cleaned = cleanSentence(text || '')
  return `${cleaned}${hasJongseong(cleaned) ? '은' : '는'}`
}

function joinIssues(...issues) {
  return issues.filter(Boolean).join(', ')
}

function extractSetupCue(description) {
  const cleaned = cleanSentence(description || '')
  if (!cleaned) return '상황이 빠르게 꼬였습니다'
  const firstSentence = cleaned.split(/[.!?]/u)[0]
  const sliced = firstSentence
    .split(/[,，]| 그러자 | 그런데 | 그리고 | 어느 날 | 그 뒤로 /u)[0]
    .trim()
  return clip(sliced || firstSentence, 36)
}

function categoryFromRuntime(runtimeCase, caseKey) {
  const relationshipType = runtimeCase?.duo?.relationshipType || runtimeCase?.meta?.relationshipType || ''
  if (relationshipType === 'tenant_landlord') return 'tenant'
  if (relationshipType === 'boss_employee') return 'workplace'
  if (relationshipType) return relationshipType
  return String(caseKey || '').split('-')[0]
}

