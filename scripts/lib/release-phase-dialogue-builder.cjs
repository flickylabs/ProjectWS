function compileReleasePhaseDialogues(root, caseKey) {
  const runtimePath = path.join(root, 'src', 'data', 'cases', 'generated', `${caseKey}.json`)
  const inputPath = path.join(root, 'src', 'data', 'cases', 'phase1-input', `${caseKey}.json`)

  if (!fs.existsSync(runtimePath)) {
    throw new Error(`missing generated case: ${path.relative(root, runtimePath).replace(/\\/g, '/')}`)
  }
  if (!fs.existsSync(inputPath)) {
    throw new Error(`missing phase input: ${path.relative(root, inputPath).replace(/\\/g, '/')}`)
  }

  const runtimeCase = readJson(runtimePath)
  const input = readJson(inputPath)
  const override = topRiskOverrides(caseKey, input, runtimeCase)
  const phase1 = override?.phase1 || buildGenericPhase1(input, runtimeCase, caseKey)
  const phase2 = override?.phase2 || buildGenericPhase2(input, runtimeCase, caseKey)

  const phase1OutPath = path.join(root, 'src', 'data', 'dialogues', 'phase1', `${caseKey}.json`)
  const phase2OutPath = path.join(root, 'src', 'data', 'dialogues', 'phase2', `${caseKey}.json`)

  writeJson(phase1OutPath, { caseId: runtimeCase.caseId, dialogues: phase1 })
  writeJson(phase2OutPath, { caseId: runtimeCase.caseId, dialogues: phase2 })

  return { caseId: runtimeCase.caseId, phase1OutPath, phase2OutPath }
}

