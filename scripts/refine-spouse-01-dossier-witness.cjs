function rewriteDossierEntry(entry) {
  const [baseKey, band] = entry.key.split(/\|(?=mid$|late$)/);
  const spec = dossierSpecs[baseKey];
  if (!spec || (band !== 'mid' && band !== 'late')) {
    return;
  }

  const texts = band === 'mid'
    ? [
        `${spec.fact} ${spec.detail}`,
        `${spec.impact} ${spec.factAlt}`,
        `오해가 커진 지점까지 포함하면, ${spec.detailAlt} ${spec.factAlt}`
      ]
    : [
        `정리해서 말씀드리면, ${spec.fact} ${spec.detailAlt}`,
        `${spec.impactAlt} ${spec.factAlt}`,
        `돌려 말하지 않겠습니다. ${spec.detail} ${spec.factAlt}`
      ];

  entry.variants.forEach((variant, index) => {
    variant.text = texts[index];
  });
}

function rewriteWitnessEntry(entry) {
  const texts = witnessRewrites[entry.key];
  if (!texts) {
    return;
  }

  entry.variants.forEach((variant, index) => {
    variant.text = texts[index];
  });
}

