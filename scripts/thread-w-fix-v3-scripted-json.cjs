function applyGenericTextFixes(text) {
  return text
    .replace(/했은데/g, '했는데')
    .replace(/았은데/g, '았는데')
    .replace(/었은데/g, '었는데')
    .replace(/왔은지/g, '왔는지')
    .replace(/들렀은지/g, '들렀는지')
    .replace(/잘못됐은지/g, '잘못됐는지')
    .replace(/했은지/g, '했는지')
    .replace(/있은/g, '있는')
    .replace(/없은/g, '없는')
    .replace(/망이진다/g, '망가진다')
    .replace(/보가게/g, '보이게')
    .replace(/보가지만/g, '보이지만')
    .replace(/보가실/g, '보이실')
    .replace(/보가던/g, '보이던')
    .replace(/40가었습니다/g, '40이었습니다')
    .replace(/묻은 날/g, '묻는 날')
    .replace(/아가러니/g, '아이러니')
    .replace(/사가에/g, '사이에')
    .replace(/정후라는 분 발걸음/g, '정후라는 분의 발걸음')
    .replace(/정후라는 분인 것도/g, '정후라는 분이었던 것도')
    .replace(/그분 손/g, '그분의 손')
}

function applyBundleFixes(bundle, relPath) {
  walkVariants(bundle, (entry, variant) => {
    variant.text = applyGenericTextFixes(applyExactTextFixes(variant.text))

    if (bundle.caseId === 'friend-v3-01' && entry.key && FRIEND_S5_REWRITES[entry.key]) {
      const rewritten = FRIEND_S5_REWRITES[entry.key]
      const idx = Number(String(variant.id).match(/-v(\d+)$/)?.[1] || 0) - 1
      if (rewritten[idx]) variant.text = rewritten[idx]
    }
  })

  if (bundle.caseId === 'friend-v3-01') {
    const found = new Set()
    for (const entry of bundle.channels?.interrogation?.entries || []) {
      if (!FRIEND_S5_REWRITES[entry.key]) continue
      found.add(entry.key)
      if ((entry.variants || []).length !== 5) {
        throw new Error(`${relPath}: expected 5 variants for ${entry.key}`)
      }
    }
    const missing = Object.keys(FRIEND_S5_REWRITES).filter((key) => !found.has(key))
    if (missing.length) {
      throw new Error(`${relPath}: missing friend S5 keys ${missing.join(', ')}`)
    }
  }
}

function audit(bundle, relPath) {
  const leftovers = []
  walkVariants(bundle, (_entry, variant) => {
    for (const token of ['준호 씨', '지연 씨', '정후 씨', '태성 씨', '수민 씨', '다은 씨', '했은데', '망이진다', '보가실', '보가게', '보가지만', '있은', '없은', '40가었습니다', '보가던', '묻은 날', '아가러니', '사가에']) {
      if (variant.text.includes(token)) leftovers.push(`${relPath}: ${variant.id}: ${token}`)
    }
  })
  return leftovers
}

function main() {
  const allLeftovers = []
  for (const relPath of TARGETS) {
    const bundle = readJson(relPath)
    applyBundleFixes(bundle, relPath)
    writeJson(relPath, bundle)
    allLeftovers.push(...audit(bundle, relPath))
    console.log(`[thread-w-fix] wrote ${relPath}`)
  }

  if (allLeftovers.length) {
    console.log('[thread-w-fix] leftover patterns:')
    for (const item of allLeftovers) console.log(item)
    process.exitCode = 1
  }
}

